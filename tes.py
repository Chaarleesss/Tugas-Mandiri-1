# 1.Penjumlahan
var1 = int(input("Masukkan bilangan penjumlahan pertama: "))
var2 = int(input("Masukkan bilangan penjumlahan kedua: "))
penjumlahan = var1 + var2
print("Hasil penjumlahan:", penjumlahan)

# 2.Pengurangan
Pengurangan1 = int(input("Masukkan bilangan pengurangan pertama: "))
Pengurangan2 = int(input("Masukkan bilangan pengurangan kedua: "))
pengurangan = Pengurangan1 - Pengurangan2
print("Hasil Pengurangan:", pengurangan)

# 3.Perkalian
Perkalian1 = int(input("Masukkan bilangan perkalian pertama: "))
Perkalian2 = int(input("Masukkan bilangan perkalian kedua: "))
perkalian = Perkalian1 * Perkalian2
print("Hasil Perkalian:", perkalian)

# 4.Perpangkatan
Perpangkatan1 = int(input("Masukkan bilangan perpangkatan pertama: "))
Perpangkatan = int(input("Masukkan bilangan perpangkatan kedua: "))
perpangkatan = Perpangkatan1 ** Perpangkatan
print("Hasil perpangkatan", perpangkatan)

# 5.Integer Division
Interdiv1 = int(input("Masukkan bilangan InterDiv pertama: "))
Interdiv2 = int(input("Masukkan bilangan InterDiv kedua: "))
InterDiv = Interdiv1 // Interdiv2
print("Hasil Integer Division:", InterDiv)

# 6.Float Division
Float1 = int(input("Masukkan bilangan FloatDiv pertama: "))
Float2 = int(input("Masukkan bilangan FloatDiv kedua: "))
FloatDiv = Float1 / Float2
print("Hasil Float Division:", FloatDiv)

# 7.Modulus
Modul1 = int(input("Masukkan bilangan Modulo pertama: "))
Modul2 = int(input("Masukkan bilangan Modulo kedua: "))
Modulo = Modul1 % Modul2
print("Hasil Modulus Division:", Modulo)